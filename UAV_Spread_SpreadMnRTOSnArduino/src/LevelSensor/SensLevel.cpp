HardwareSerial SerialCom (USART2);

UARTSensLevelClass::UARTSensLevelClass(uint32_t baudrate) {
    this->baudrate = baudrate;
    init();
}

void UARTSensLevelClass::init() {
    SerialCom.begin(baudrate);
}

void UARTSensLevelClass::sendCMD(uint8_t cmd[], uint8_t length)
{
    for (uint8_t y = 0; y < length; y++)
    {
        byteSend = cmd[y];
        SerialCom.write(byteSend);
        SerialCom.flush();
        //delayMicroseconds(30);
    } 
    //delay(5);
}

